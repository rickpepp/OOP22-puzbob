package it.unibo.puzbob.model;

/**
 * This is an enum that show the possible state of the game
 */
public enum GameStatus {
    /**
     * The game is running
     */
    RUNNING,
    /**
     * The game is ended and the player win
     */
    WIN,
    /**
     * The game is ended and the player lose
     */
    LOST
}
